function start_post($start_pk){
    global $arr;
    global $post_pk;
    $parent;
    foreach ($arr as $post) {
        if($post["post_pk"] == $post_pk){
            $parent = $post;
        }
    }
    if(isset($parent)){
        post($parent, 1);
    }
}


