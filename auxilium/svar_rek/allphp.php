function post($parent, $counter){
    start_div($parent, $counter);

    global $arr;
    //var_dump($parent);
    $children = calc_children($parent);

    if(!empty($children)){
        $counter++;
        foreach ($children as &$child) {
            post($child, $counter);
        }
    }

    end_div();
}


//                  Tar fram föräldrerns barn
function calc_children($parent){
    global $arr;
    $children = [];

    foreach ($arr as &$q) {
        if($q['post_fk'] == $parent['post_pk']){
            array_push($children, $q);
        }
    }
    return $children;
}


